SYSTEM DROP  TABLE IF EXISTS src;

SYSTEM DROP  TABLE IF EXISTS dst;

SYSTEM DROP  TABLE IF EXISTS original_mv;

SYSTEM DROP  TABLE IF EXISTS new_mv;

CREATE TABLE src
(
    x UInt8
)
ENGINE = Null;

CREATE TABLE dst
(
    x UInt8
)
ENGINE = Memory;

CREATE MATERIALIZED VIEW original_mv
TO dst
AS
SELECT *
FROM src;

INSERT INTO src;

SELECT *
FROM original_mv
ORDER BY x ASC;

INSERT INTO src;

SELECT *
FROM dst
ORDER BY x ASC;

SELECT *
FROM new_mv
ORDER BY x ASC;